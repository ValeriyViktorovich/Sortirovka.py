sentence = "Hello, World!"
upper_case_text = sentence.upper() #Translate in upper registor
lower_case_text = sentence.lower() #Translate in lower registor
swapped_case_text = sentence.swapcase() #Change registor word difference
capital_case_text = sentence.capitalize() #Change first word  in upper registor, other words are in lower
print(upper_case_text,lower_case_text,swapped_case_text,capital_case_text)
